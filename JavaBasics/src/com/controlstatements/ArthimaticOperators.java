package com.controlstatements;

public class ArthimaticOperators {
	public static void main(String[] args) {
		int x=10,y=20,z;
		z=x+y;
		System.out.println(" "+ z);
		z=x-y;
		System.out.println(" "+ z);
		z=x*y;
		System.out.println(" "+ z);
		z=x%y;
		System.out.println(" "+ z);
		z=++x;
		System.out.println(" "+ z);
		z=x++;
		System.out.println(" "+ z);
	}
	

}
